#include "libft.h"

int	ft_toupper(int c)
{
	unsigned char	chr;

	if ((c > -129 && c < -1) || (c > -1 && c < 256))
	{
		chr = c;
		if (chr >= 'a' && chr <= 'z')
		chr -= 32;
		return (chr);
	}
	return (c);
}
